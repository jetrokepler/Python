nota1 = float(input("Informe a primeira nota para realizar a média: "))
nota2 = float(input("Informe a segunda nota para realizar a média: "))
nota3 = float(input("Informe a terceira nota par realizar a média: "))

result = (nota1 + nota2 + nota3) / 3

print('Resultado da média: {:.2f}'.format(result))